import { appendFile, mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const MARKER = '<!-- leogriel-regression-report -->';

export function normalizeTestResult(value) {
  if (!value || typeof value !== 'object' || Array.isArray(value)) return failureResult('Result is not a JSON object');
  const envelope = value;
  const result = envelope.schemaVersion === 1 && envelope.command === 'test' ? envelope.data : envelope;
  if (!result || typeof result !== 'object' || Array.isArray(result)) {
    const message = firstMessage(envelope.errors) || 'Leogriel did not return a behavioral test result';
    return failureResult(message);
  }
  const verdict = ['improved', 'unchanged', 'regressed', 'inconclusive'].includes(result.verdict)
    ? result.verdict
    : 'inconclusive';
  return {
    ...result,
    verdict,
    cases: Array.isArray(result.cases) ? result.cases : [],
    warnings: [
      ...(Array.isArray(envelope.warnings) ? envelope.warnings : []),
      ...(result.warning ? [result.warning] : []),
    ].map(messageOf).filter(Boolean),
    errors: Array.isArray(envelope.errors) ? envelope.errors.map(messageOf).filter(Boolean) : [],
  };
}

export function renderMarkdown(result) {
  const comparison = result.comparison;
  const metadata = [
    ['Verdict', verdictLabel(result.verdict)],
    ['Skill', inline(result.skill || 'unknown')],
    ['Runner', inline(result.runner || 'unknown')],
    ['Requested model', inline(result.requestedModel || 'not pinned')],
    ['Resolved models', inline(Array.isArray(result.resolvedModels) && result.resolvedModels.length ? result.resolvedModels.join(', ') : 'not reported')],
    ['Runs per case', String(result.runs ?? 'unknown')],
    ['Reference', comparison ? `${inline(comparison.requestedRef)} → ${inline(shortSha(comparison.commit))}` : 'no-skill baseline'],
    ['Pass rate', `${percent(result.baselinePassRate)} → ${percent(result.skillPassRate)}`],
  ];
  const lines = [MARKER, '# Leogriel behavioral regression', '', ...metadata.map(([name, value]) => `- **${name}:** ${value}`), ''];
  if (comparison) {
    lines.push(
      `- **Reference integrity:** ${inline(comparison.referenceIntegrity || 'unknown')}`,
      `- **Candidate integrity:** ${inline(comparison.candidateIntegrity || 'unknown')}`,
      '',
    );
  }
  lines.push('## Test cases', '', '| Case | Verdict | Reference | Candidate |', '|---|---:|---:|---:|');
  if (!result.cases.length) lines.push('| No completed cases | inconclusive | — | — |');
  for (const item of result.cases) {
    lines.push(`| ${escapeTable(item.name || 'unnamed')} | ${escapeTable(item.verdict || 'inconclusive')} | ${percent(item.baselinePassRate)} | ${percent(item.skillPassRate)} |`);
  }
  appendMessages(lines, 'Warnings', result.warnings);
  appendMessages(lines, 'Errors', result.errors);
  lines.push('', `<sub>Generated by Leogriel${result.leogrielVersion ? ` ${escapeMarkdown(result.leogrielVersion)}` : ''}. Behavioral tests execute agent and trusted test code; isolation is not an absolute security sandbox.</sub>`, '');
  return lines.join('\n');
}

export function renderHtml(result, markdown) {
  const rows = result.cases.length
    ? result.cases.map((item) => `<tr><td>${html(item.name || 'unnamed')}</td><td>${html(item.verdict || 'inconclusive')}</td><td>${percent(item.baselinePassRate)}</td><td>${percent(item.skillPassRate)}</td></tr>`).join('\n')
    : '<tr><td>No completed cases</td><td>inconclusive</td><td>—</td><td>—</td></tr>';
  return `<!doctype html>
<html lang="en"><head><meta charset="utf-8"><meta name="viewport" content="width=device-width,initial-scale=1">
<title>Leogriel regression — ${html(result.verdict)}</title>
<style>body{font:16px/1.5 system-ui,sans-serif;max-width:960px;margin:2rem auto;padding:0 1rem;color:#1f2937}h1{color:#6d28d9}.verdict{display:inline-block;padding:.3rem .65rem;border-radius:999px;background:#ede9fe;font-weight:700}table{border-collapse:collapse;width:100%;margin:1rem 0}th,td{border:1px solid #d1d5db;padding:.55rem;text-align:left}th{background:#f3f4f6}code{overflow-wrap:anywhere}pre{white-space:pre-wrap;background:#f8fafc;padding:1rem;border-radius:.5rem}small{color:#64748b}</style></head>
<body><h1>Leogriel behavioral regression</h1><p class="verdict">${html(result.verdict)}</p>
<ul><li><strong>Skill:</strong> ${html(result.skill || 'unknown')}</li><li><strong>Runner:</strong> ${html(result.runner || 'unknown')}</li><li><strong>Runs per case:</strong> ${html(String(result.runs ?? 'unknown'))}</li><li><strong>Pass rate:</strong> ${percent(result.baselinePassRate)} → ${percent(result.skillPassRate)}</li></ul>
<table><thead><tr><th>Case</th><th>Verdict</th><th>Reference</th><th>Candidate</th></tr></thead><tbody>${rows}</tbody></table>
<details><summary>Markdown source</summary><pre>${html(markdown)}</pre></details>
<small>Generated by Leogriel. Isolation is not an absolute security sandbox.</small></body></html>\n`;
}

export function badgeDocument(verdict) {
  const color = { improved: 'brightgreen', unchanged: 'blue', regressed: 'red', inconclusive: 'orange' }[verdict] || 'lightgrey';
  return { schemaVersion: 1, label: 'Leogriel', message: verdict, color };
}

async function main(argv = process.argv.slice(2), environment = process.env) {
  const input = argv[0];
  if (!input) throw new Error('Usage: render-test-report.mjs <result.json> --markdown <path> --html <path> --badge <path> [--summary]');
  const markdownPath = option(argv, '--markdown');
  const htmlPath = option(argv, '--html');
  const badgePath = option(argv, '--badge');
  if (!markdownPath || !htmlPath || !badgePath) throw new Error('--markdown, --html, and --badge are required');
  let parsed;
  try {
    parsed = JSON.parse(await readFile(input, 'utf8'));
  } catch {
    parsed = { schemaVersion: 1, ok: false, command: 'test', errors: [{ message: 'Leogriel result was missing or invalid JSON' }] };
    await mkdir(dirname(resolve(input)), { recursive: true });
    await writeFile(input, `${JSON.stringify(parsed, null, 2)}\n`);
  }
  const result = normalizeTestResult(parsed);
  const markdown = renderMarkdown(result);
  const htmlReport = renderHtml(result, markdown);
  await Promise.all([
    write(markdownPath, `${markdown}\n`),
    write(htmlPath, htmlReport),
    write(badgePath, `${JSON.stringify(badgeDocument(result.verdict), null, 2)}\n`),
  ]);
  if (argv.includes('--summary') && environment.GITHUB_STEP_SUMMARY) await appendFile(environment.GITHUB_STEP_SUMMARY, `${markdown}\n`);
  if (environment.GITHUB_OUTPUT) await appendFile(environment.GITHUB_OUTPUT, `verdict=${result.verdict}\n`);
  return result;
}

function failureResult(message) { return { verdict: 'inconclusive', cases: [], warnings: [], errors: [message] }; }
function option(argv, name) { const index = argv.indexOf(name); return index >= 0 ? argv[index + 1] : undefined; }
async function write(path, content) { const target = resolve(path); await mkdir(dirname(target), { recursive: true }); await writeFile(target, content, { flag: 'wx' }); }
function messageOf(value) { return typeof value === 'string' ? value : value && typeof value.message === 'string' ? value.message : ''; }
function firstMessage(values) { return Array.isArray(values) ? values.map(messageOf).find(Boolean) : undefined; }
function shortSha(value) { return typeof value === 'string' ? value.slice(0, 12) : 'unknown'; }
function percent(value) { return typeof value === 'number' && Number.isFinite(value) ? `${(value * 100).toFixed(1)}%` : '—'; }
function verdictLabel(value) { return { improved: '✅ improved', unchanged: '➖ unchanged', regressed: '❌ regressed', inconclusive: '⚠️ inconclusive' }[value] || '⚠️ inconclusive'; }
function inline(value) { return `\`${escapeMarkdown(String(value)).replace(/`/g, '\\`')}\``; }
function escapeTable(value) { return escapeMarkdown(String(value)).replace(/\|/g, '\\|').replace(/\r?\n/g, ' '); }
function escapeMarkdown(value) { return String(value).replace(/[<>]/g, (character) => character === '<' ? '&lt;' : '&gt;'); }
function html(value) { return String(value).replace(/[&<>"']/g, (character) => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;' })[character]); }
function appendMessages(lines, title, values) { if (Array.isArray(values) && values.length) lines.push('', `## ${title}`, '', ...values.map((value) => `- ${escapeMarkdown(value)}`)); }

const isMain = process.argv[1] && resolve(process.argv[1]) === fileURLToPath(import.meta.url);
if (isMain) main().catch((error) => { process.stderr.write(`${error.message}\n`); process.exitCode = 2; });

export { main, MARKER };
